# Jenis-jenis Operator
# 1. Operator Aritmatika
aritx = 11
arity = 5
print(aritx + arity)
print(aritx - arity)
print(aritx * arity)
print(aritx // arity)
print(aritx / arity)
print(aritx % arity)
print(aritx ** arity)

# 2. Operator Relational
relx = 5
rely = 10
print(relx == rely)
print(relx != rely)
print(relx > rely)
print(relx < rely)
print(relx >= rely)
print(relx <= rely)

# 3. Operator Logika
# And
print(True and True)
print(True and False)
print(False and True)
print(False and False)

# Or
print(True or True)
print(True or False)
print(False or True)
print(False or False)

# Not
print(not True)
print(not False)

